        DateTimeFormatter tf = DateTimeFormatter.ofPattern("HH:mm");
        date = LocalDate.parse(datefeld.getText(),df);
        time = LocalTime.parse(timefeld.getText(),tf);
        kennzeichen = kennfeld.getText();
        vgemessen = Integer.parseInt(vfeld.getText());
        verlaubt = Integer.parseInt(v2feld.getText());
        messung = new Geschwindigkeitsmessung(kennzeichen, date, time, verlaubt, vgemessen);
        check=true;
        this.dispose();
